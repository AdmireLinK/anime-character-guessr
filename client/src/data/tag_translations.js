export function getTranslatedTag(tag, locale = 'zh') {
  if (typeof tag !== 'string') return tag;
  return TAG_TRANSLATIONS[locale]?.[tag] || tag;
}

export function hasTagTranslation(tag, locale = 'zh') {
  return typeof tag === 'string' && Object.prototype.hasOwnProperty.call(TAG_TRANSLATIONS[locale] || {}, tag);
}
